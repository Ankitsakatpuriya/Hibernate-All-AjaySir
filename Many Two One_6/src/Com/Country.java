package Com;

public class Country {
	
	private int sid;
	private String sname;
	private World sobject;
	
	
	
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public World getSobject() {
		return sobject;
	}
	public void setSobject(World sobject) {
		this.sobject = sobject;
	}
}
