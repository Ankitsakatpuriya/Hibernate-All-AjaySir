package Com;

public class Company {
	
	private int sid;
	private String sname;
	private India sobject;
	
	
	
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public India getSobject() {
		return sobject;
	}
	public void setSobject(India sobject) {
		this.sobject = sobject;
	}
}
