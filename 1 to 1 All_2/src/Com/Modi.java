package Com;

public class Modi {
	private int id;
	private String name;
	private BJP Party;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public BJP getParty() {
		return Party;
	}
	public void setParty(BJP party) {
		Party = party;
	}
	

}
