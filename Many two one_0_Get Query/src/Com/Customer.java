package Com;

public class Customer {
	
	private int cid;
	private String cname;
	private Vendor pobject;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public Vendor getPobject() {
		return pobject;
	}
	public void setPobject(Vendor pobject) {
		this.pobject = pobject;
	}
	
	
	

}
